package com.adequate.beans;

public class Person {
	
	private int userID;
	private String email;
	private String password;
	
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public int getUserID() {
		return userID;
	}
	public String getEmail() {
		return email;
	}
	
	
}
